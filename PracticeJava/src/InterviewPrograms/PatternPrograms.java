package InterviewPrograms;

public class PatternPrograms {

	
	
	
	
	
	
	public static void Squer(){
		
		for(char i='E';i>='A';i--){
			
			
			
			for(char j='A';j<='E';j++){
				System.out.print(" ");
				System.out.print(i);
				
			}
			
			System.out.println("\n");
		}
		
		
		
	}
	
	
	public static void main(String[] args) {
		
		
		Squer();
	}
	
	
	
	
	
	
	
}
